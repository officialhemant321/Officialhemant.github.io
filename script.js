function renderSlide() {
  const slide = slides[currentSlide];

  slideContainer.innerHTML = `
    <article class="slide" aria-labelledby="slideTitle">
      <header>
        <p class="eyebrow">Academic Presentation Slide</p>
        <h2 id="slideTitle">${escapeHTML(slide.title)}</h2>
        <p class="slide-subtitle">${escapeHTML(slide.subtitle)}</p>
      </header>

      <div class="slide-content">
        <ul>
          ${slide.bullets.map(item => `<li>${escapeHTML(item)}</li>`).join("")}
        </ul>
      </div>

      <footer class="slide-footer">
        <span class="citation">${escapeHTML(slide.citation)}</span>
        <span>${currentSlide + 1} / ${slides.length}</span>
      </footer>
    </article>
  `;

  slideIndicator.textContent = `Slide ${currentSlide + 1} of ${slides.length}`;
  progressBar.style.width = `${((currentSlide + 1) / slides.length) * 100}%`;
  prevButton.disabled = currentSlide === 0;
  nextButton.disabled = currentSlide === slides.length - 1;
}

function nextSlide() {
  if (currentSlide < slides.length - 1) {
    currentSlide += 1;
    renderSlide();
  }
}

function prevSlide() {
  if (currentSlide > 0) {
    currentSlide -= 1;
    renderSlide();
  }
}

function firstSlide() {
  currentSlide = 0;
  renderSlide();
}

function lastSlide() {
  currentSlide = slides.length - 1;
  renderSlide();
}

